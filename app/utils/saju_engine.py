def _josa(word: str, type_: str) -> str:
    last = ord(word[-1])
    has_jongseong = (last - 0xAC00) % 28 != 0 if 0xAC00 <= last <= 0xD7A3 else False
    if type_ == "이/가":
        return "이" if has_jongseong else "가"
    if type_ == "을/를":
        return "을" if has_jongseong else "를"
    return ""


def _rank_to_grade(rank: int) -> str:
    if rank <= 3:
        return "대길"
    if rank <= 6:
        return "길"
    if rank <= 9:
        return "평"
    return "흉"


def get_day_cheongan(d: date) -> str:
    base = date(2000, 1, 1)   # 2000-01-01 = 갑(甲)
    return CHEONGAN[(d - base).days % 10]


def get_day_jiji(d: date) -> str:
    base = date(2000, 1, 1)   # 2000-01-01 = 자(子)
    return JIJI[(d - base).days % 12]


def get_daily_theme(d: date) -> str:
    gan = get_day_cheongan(d)
    ji  = get_day_jiji(d)
    desc = CHEONGAN_DESC[gan]
    return f"{gan}{ji}일 — {desc}"


def get_lucky_item(sign: str, d: date) -> str:
    ohang = ZODIAC_OHANG[sign]
    rng = random.Random(f"{d.isoformat()}-{sign}-lucky")
    return rng.choice(LUCKY_ITEMS[ohang])


def get_fortune_text(sign: str, rank: int, d: date) -> str:
    grade = _rank_to_grade(rank)
    ohang = ZODIAC_OHANG[sign]
    templates = FORTUNE_TEMPLATES[grade][ohang]
    rng = random.Random(f"{d.isoformat()}-{sign}-fortune")
    text = rng.choice(templates)

    # 상생상극 부가 설명 (1~3위 / 10~12위)
    day_ohang = CHEONGAN_OHANG[get_day_cheongan(d)]
    if rank <= 3 and SANGSAENG.get(day_ohang) == ohang:
        text += f" ({day_ohang}{_josa(day_ohang, '이/가')} {ohang}{_josa(ohang, '을/를')} 생하는 날)"
    elif rank >= 10 and SANGGEUK.get(day_ohang) == ohang:
        text += f" ({day_ohang}{_josa(day_ohang, '이/가')} {ohang}{_josa(ohang, '을/를')} 극하는 날)"

    return text


def generate_all_fortunes(rankings: list[str], d: date) -> dict[str, str]:
    return {sign: get_fortune_text(sign, rank + 1, d) for rank, sign in enumerate(rankings)}
